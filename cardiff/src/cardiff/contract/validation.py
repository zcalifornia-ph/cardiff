"""Canonical request validation for Cardiff."""

from __future__ import annotations

from collections.abc import Iterable, Mapping
from pathlib import Path
import re
from urllib.parse import urlparse

from cardiff.paths import normalize_approved_asset_roots

from .errors import ValidationCode, ValidationOutcome
from .models import (
    AssetReference,
    IdentityProfile,
    RenderRequest,
    TemplateOptionValue,
    TemplateSelection,
    ValidationIssue,
    ValidationResult,
)
from .sanitization import UnsafeContentError, normalize_text, sanitize_text

IDENTITY_REQUIRED_FIELDS = ("full_name", "role", "email")
IDENTITY_OPTIONAL_FIELDS = ("phone", "organization", "department", "website", "pronouns")
IDENTITY_ALLOWED_FIELDS = frozenset((*IDENTITY_REQUIRED_FIELDS, *IDENTITY_OPTIONAL_FIELDS, "address_lines"))
TOP_LEVEL_FIELDS = frozenset({"identity", "template", "assets"})
TEMPLATE_FIELDS = frozenset({"id", "options"})
TEMPLATE_OPTION_RULES = {
    "variant": str,
    "include_qr": bool,
    "accent_hex": str,
}
ASSET_SLOTS = frozenset({"logo", "avatar"})
ALLOWED_ASSET_EXTENSIONS = frozenset({".png", ".jpg", ".jpeg", ".svg", ".pdf"})
SYMBOLIC_ID_PATTERN = re.compile(r"^[a-z0-9][a-z0-9_-]*$")
HEX_COLOR_PATTERN = re.compile(r"^#[0-9a-fA-F]{6}$")


def classify_validation_result(issues: Iterable[ValidationIssue]) -> ValidationOutcome:
    """Map a set of issues to accepted or rejected status."""

    return ValidationOutcome.REJECTED if tuple(issues) else ValidationOutcome.ACCEPTED


def validate_render_request(
    payload: Mapping[str, object],
    *,
    approved_asset_roots: Iterable[str | Path] | None = None,
    source_name: str = "<memory>",
    source_format: str = "yaml",
) -> ValidationResult:
    """Validate a parsed mapping and return a sanitized canonical request."""

    issues: list[ValidationIssue] = []
    approved_roots = normalize_approved_asset_roots(tuple(approved_asset_roots or ()))

    if not isinstance(payload, Mapping):
        issues.append(
            ValidationIssue(
                code=ValidationCode.CONTRACT_ERROR,
                field="$",
                message="the request document must decode into a mapping",
            )
        )
        return ValidationResult(outcome=ValidationOutcome.REJECTED, issues=_finalize_issues(issues))

    unknown_top_level = sorted(set(payload) - TOP_LEVEL_FIELDS)
    issues.extend(
        ValidationIssue(
            code=ValidationCode.UNKNOWN_FIELD,
            field=field,
            message=f"unsupported top-level field '{field}'",
        )
        for field in unknown_top_level
    )

    identity_data = payload.get("identity")
    identity_fields, identity_issues = _validate_identity(identity_data)
    issues.extend(identity_issues)

    template_data = payload.get("template")
    template_selection, template_issues = _validate_template(template_data)
    issues.extend(template_issues)

    assets_data = payload.get("assets", {})
    assets, asset_issues = _validate_assets(assets_data, approved_roots)
    issues.extend(asset_issues)

    if issues:
        return ValidationResult(outcome=ValidationOutcome.REJECTED, issues=_finalize_issues(issues))

    request = RenderRequest(
        identity=IdentityProfile(**identity_fields),
        template=template_selection,
        assets=assets,
        source_name=source_name,
        source_format=source_format,
    )
    return ValidationResult(outcome=ValidationOutcome.ACCEPTED, request=request)


def sanitize_render_fields(render_request: RenderRequest) -> RenderRequest:
    """Apply the canonical sanitization rules to an already-typed request."""

    identity = render_request.identity
    sanitized_identity = IdentityProfile(
        full_name=sanitize_text(identity.full_name, field_name="identity.full_name"),
        role=sanitize_text(identity.role, field_name="identity.role"),
        email=sanitize_text(identity.email, field_name="identity.email"),
        phone=_sanitize_optional_text(identity.phone, "identity.phone"),
        organization=_sanitize_optional_text(identity.organization, "identity.organization"),
        department=_sanitize_optional_text(identity.department, "identity.department"),
        website=_sanitize_optional_text(identity.website, "identity.website"),
        address_lines=tuple(
            sanitize_text(line, field_name=f"identity.address_lines[{index}]")
            for index, line in enumerate(identity.address_lines)
        ),
        pronouns=_sanitize_optional_text(identity.pronouns, "identity.pronouns"),
    )

    sanitized_options: dict[str, TemplateOptionValue] = {}
    for option_name, option_value in render_request.template.options.items():
        if isinstance(option_value, str):
            if option_name == "accent_hex":
                sanitized_options[option_name] = normalize_text(option_value)
            else:
                sanitized_options[option_name] = sanitize_text(
                    option_value,
                    field_name=f"template.options.{option_name}",
                )
        else:
            sanitized_options[option_name] = option_value

    return RenderRequest(
        identity=sanitized_identity,
        template=TemplateSelection(
            template_id=normalize_text(render_request.template.template_id),
            options=sanitized_options,
        ),
        assets=render_request.assets,
        source_name=render_request.source_name,
        source_format=render_request.source_format,
    )


def validate_asset_reference(
    asset_reference: AssetReference,
    approved_roots: Iterable[str | Path],
) -> tuple[ValidationIssue, ...]:
    """Validate one asset path against approved roots."""

    slot = asset_reference.slot
    candidate = asset_reference.path
    resolved_roots = normalize_approved_asset_roots(tuple(approved_roots))

    if slot not in ASSET_SLOTS:
        return _finalize_issues(
            [
                ValidationIssue(
                    code=ValidationCode.UNKNOWN_FIELD,
                    field=f"assets.{slot}",
                    message=f"unsupported asset slot '{slot}'",
                )
            ]
        )

    parsed = urlparse(candidate)
    if parsed.scheme and not re.match(r"^[a-zA-Z]:$", parsed.scheme):
        return _finalize_issues(
            [
                ValidationIssue(
                    code=ValidationCode.INVALID_ASSET_PATH,
                    field=f"assets.{slot}",
                    message="asset paths must be local files inside approved roots",
                )
            ]
        )

    asset_path = Path(candidate)
    if asset_path.is_absolute():
        normalized = asset_path.resolve()
        if not any(normalized.is_relative_to(root) for root in resolved_roots):
            return _finalize_issues(
                [
                    ValidationIssue(
                        code=ValidationCode.INVALID_ASSET_PATH,
                        field=f"assets.{slot}",
                        message="absolute asset paths must stay inside an approved root",
                    )
                ]
            )
    else:
        if not resolved_roots:
            return _finalize_issues(
                [
                    ValidationIssue(
                        code=ValidationCode.INVALID_ASSET_PATH,
                        field=f"assets.{slot}",
                        message="relative asset paths require at least one approved root",
                    )
                ]
            )
        if ".." in asset_path.parts:
            return _finalize_issues(
                [
                    ValidationIssue(
                        code=ValidationCode.INVALID_ASSET_PATH,
                        field=f"assets.{slot}",
                        message="asset paths must not use parent traversal",
                    )
                ]
            )

    if asset_path.suffix.lower() not in ALLOWED_ASSET_EXTENSIONS:
        return _finalize_issues(
            [
                ValidationIssue(
                    code=ValidationCode.INVALID_ASSET_PATH,
                    field=f"assets.{slot}",
                    message=(
                        "asset paths must use one of: "
                        + ", ".join(sorted(ALLOWED_ASSET_EXTENSIONS))
                    ),
                )
            ]
        )

    return ()


def _validate_identity(payload: object) -> tuple[dict[str, object], list[ValidationIssue]]:
    issues: list[ValidationIssue] = []
    if payload is None:
        return {}, [
            ValidationIssue(
                code=ValidationCode.MISSING_REQUIRED,
                field="identity",
                message="identity is required",
            )
        ]
    if not isinstance(payload, Mapping):
        return {}, [
            ValidationIssue(
                code=ValidationCode.INVALID_TYPE,
                field="identity",
                message="identity must be an object",
            )
        ]

    issues.extend(
        ValidationIssue(
            code=ValidationCode.UNKNOWN_FIELD,
            field=f"identity.{field}",
            message=f"unsupported identity field '{field}'",
        )
        for field in sorted(set(payload) - IDENTITY_ALLOWED_FIELDS)
    )

    identity_fields: dict[str, object] = {}
    for field_name in IDENTITY_REQUIRED_FIELDS:
        raw_value = payload.get(field_name)
        value, field_issues = _validate_text_field(
            raw_value,
            field_name=f"identity.{field_name}",
            required=True,
            multiline=False,
        )
        issues.extend(field_issues)
        if value is not None:
            identity_fields[field_name] = value

    for field_name in IDENTITY_OPTIONAL_FIELDS:
        raw_value = payload.get(field_name)
        value, field_issues = _validate_text_field(
            raw_value,
            field_name=f"identity.{field_name}",
            required=False,
            multiline=False,
        )
        issues.extend(field_issues)
        if value is not None:
            identity_fields[field_name] = value

    address_lines, address_issues = _validate_address_lines(payload.get("address_lines"))
    issues.extend(address_issues)
    if address_lines:
        identity_fields["address_lines"] = address_lines

    return identity_fields, issues


def _validate_template(payload: object) -> tuple[TemplateSelection, list[ValidationIssue]]:
    issues: list[ValidationIssue] = []
    if payload is None:
        return TemplateSelection(template_id=""), [
            ValidationIssue(
                code=ValidationCode.MISSING_REQUIRED,
                field="template",
                message="template is required",
            )
        ]
    if not isinstance(payload, Mapping):
        return TemplateSelection(template_id=""), [
            ValidationIssue(
                code=ValidationCode.INVALID_TYPE,
                field="template",
                message="template must be an object",
            )
        ]

    issues.extend(
        ValidationIssue(
            code=ValidationCode.UNKNOWN_FIELD,
            field=f"template.{field}",
            message=f"unsupported template field '{field}'",
        )
        for field in sorted(set(payload) - TEMPLATE_FIELDS)
    )

    template_id = payload.get("id")
    if template_id is None:
        issues.append(
            ValidationIssue(
                code=ValidationCode.MISSING_REQUIRED,
                field="template.id",
                message="template.id is required",
            )
        )
    elif not isinstance(template_id, str):
        issues.append(
            ValidationIssue(
                code=ValidationCode.INVALID_TEMPLATE,
                field="template.id",
                message="template.id must be a string",
            )
        )
    else:
        normalized_template_id = normalize_text(template_id)
        if not SYMBOLIC_ID_PATTERN.fullmatch(normalized_template_id):
            issues.append(
                ValidationIssue(
                    code=ValidationCode.INVALID_TEMPLATE,
                    field="template.id",
                    message="template.id must be a symbolic slug such as 'business-card'",
                )
            )
        template_id = normalized_template_id

    options_payload = payload.get("options", {})
    if not isinstance(options_payload, Mapping):
        issues.append(
            ValidationIssue(
                code=ValidationCode.INVALID_TYPE,
                field="template.options",
                message="template.options must be an object when present",
            )
        )
        options_payload = {}

    options: dict[str, TemplateOptionValue] = {}
    issues.extend(
        ValidationIssue(
            code=ValidationCode.UNKNOWN_FIELD,
            field=f"template.options.{field}",
            message=f"unsupported template option '{field}'",
        )
        for field in sorted(set(options_payload) - set(TEMPLATE_OPTION_RULES))
    )

    for option_name, expected_type in TEMPLATE_OPTION_RULES.items():
        if option_name not in options_payload:
            continue
        raw_value = options_payload[option_name]
        if not isinstance(raw_value, expected_type):
            issues.append(
                ValidationIssue(
                    code=ValidationCode.INVALID_TYPE,
                    field=f"template.options.{option_name}",
                    message=f"{option_name} must be of type {expected_type.__name__}",
                )
            )
            continue

        if option_name == "variant":
            normalized_option = normalize_text(raw_value)
            if not SYMBOLIC_ID_PATTERN.fullmatch(normalized_option):
                issues.append(
                    ValidationIssue(
                        code=ValidationCode.INVALID_TEMPLATE,
                        field=f"template.options.{option_name}",
                        message=f"{option_name} must be a symbolic slug",
                    )
                )
                continue
            options[option_name] = normalized_option
            continue

        if option_name == "accent_hex":
            normalized_option = normalize_text(raw_value)
            if not HEX_COLOR_PATTERN.fullmatch(normalized_option):
                issues.append(
                    ValidationIssue(
                        code=ValidationCode.INVALID_TEMPLATE,
                        field="template.options.accent_hex",
                        message="accent_hex must be a six-digit hex color such as '#0055AA'",
                    )
                )
                continue
            options[option_name] = normalized_option
            continue

        options[option_name] = raw_value

    return (
        TemplateSelection(template_id=template_id if isinstance(template_id, str) else "", options=options),
        issues,
    )


def _validate_assets(
    payload: object,
    approved_roots: tuple[Path, ...],
) -> tuple[dict[str, AssetReference], list[ValidationIssue]]:
    issues: list[ValidationIssue] = []
    if payload is None:
        return {}, []
    if not isinstance(payload, Mapping):
        return {}, [
            ValidationIssue(
                code=ValidationCode.INVALID_TYPE,
                field="assets",
                message="assets must be an object when present",
            )
        ]

    assets: dict[str, AssetReference] = {}
    for slot in sorted(payload):
        raw_value = payload[slot]
        if slot not in ASSET_SLOTS:
            issues.append(
                ValidationIssue(
                    code=ValidationCode.UNKNOWN_FIELD,
                    field=f"assets.{slot}",
                    message=f"unsupported asset slot '{slot}'",
                )
            )
            continue
        if not isinstance(raw_value, str):
            issues.append(
                ValidationIssue(
                    code=ValidationCode.INVALID_TYPE,
                    field=f"assets.{slot}",
                    message=f"assets.{slot} must be a string path",
                )
            )
            continue

        normalized_path = normalize_text(raw_value)
        reference = AssetReference(slot=slot, path=normalized_path)
        asset_issues = validate_asset_reference(reference, approved_roots)
        if asset_issues:
            issues.extend(asset_issues)
            continue
        assets[slot] = AssetReference(slot=slot, path=Path(normalized_path).as_posix())

    return assets, issues


def _validate_address_lines(payload: object) -> tuple[tuple[str, ...], list[ValidationIssue]]:
    issues: list[ValidationIssue] = []
    if payload is None:
        return (), []
    if not isinstance(payload, list):
        return (), [
            ValidationIssue(
                code=ValidationCode.INVALID_TYPE,
                field="identity.address_lines",
                message="address_lines must be a list of strings",
            )
        ]
    lines: list[str] = []
    for index, line in enumerate(payload):
        if not isinstance(line, str):
            issues.append(
                ValidationIssue(
                    code=ValidationCode.INVALID_TYPE,
                    field=f"identity.address_lines[{index}]",
                    message="address line entries must be strings",
                )
            )
            continue
        value, field_issues = _validate_text_field(
            line,
            field_name=f"identity.address_lines[{index}]",
            required=True,
            multiline=False,
        )
        issues.extend(field_issues)
        if value is not None:
            lines.append(value)
    return tuple(lines), issues


def _validate_text_field(
    raw_value: object,
    *,
    field_name: str,
    required: bool,
    multiline: bool,
) -> tuple[str | None, list[ValidationIssue]]:
    issues: list[ValidationIssue] = []

    if raw_value is None:
        if required:
            issues.append(
                ValidationIssue(
                    code=ValidationCode.MISSING_REQUIRED,
                    field=field_name,
                    message=f"{field_name} is required",
                )
            )
        return None, issues

    if not isinstance(raw_value, str):
        issues.append(
            ValidationIssue(
                code=ValidationCode.INVALID_TYPE,
                field=field_name,
                message=f"{field_name} must be a string",
            )
        )
        return None, issues

    if not multiline and ("\n" in raw_value or "\r" in raw_value):
        issues.append(
            ValidationIssue(
                code=ValidationCode.INVALID_TYPE,
                field=field_name,
                message=f"{field_name} must be a single-line string",
            )
        )
        return None, issues

    normalized = normalize_text(raw_value, multiline=multiline)
    if not normalized:
        if required:
            issues.append(
                ValidationIssue(
                    code=ValidationCode.MISSING_REQUIRED,
                    field=field_name,
                    message=f"{field_name} must not be empty",
                )
            )
        return None, issues

    if field_name.endswith("email") and "@" not in normalized:
        issues.append(
            ValidationIssue(
                code=ValidationCode.INVALID_TYPE,
                field=field_name,
                message="identity.email must look like an email address",
            )
        )
        return None, issues

    try:
        sanitized = sanitize_text(normalized, field_name=field_name)
    except UnsafeContentError as error:
        issues.append(
            ValidationIssue(
                code=ValidationCode.UNSAFE_CONTENT,
                field=field_name,
                message=str(error),
            )
        )
        return None, issues

    return sanitized, issues


def _finalize_issues(issues: Iterable[ValidationIssue]) -> tuple[ValidationIssue, ...]:
    return tuple(sorted(issues, key=lambda issue: (issue.field, issue.code, issue.message)))


def _sanitize_optional_text(value: str | None, field_name: str) -> str | None:
    if value is None:
        return None
    return sanitize_text(value, field_name=field_name)
