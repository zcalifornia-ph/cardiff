"""Operator-facing CLI for Cardiff."""

from __future__ import annotations

import argparse
import json
from dataclasses import dataclass
from pathlib import Path
import sys
from typing import TextIO

from cardiff.contract import load_request_document, parse_render_request
from cardiff.rendering import (
    DeterministicTeXAdapter,
    get_default_tex_adapter,
    render_request_to_pdf,
)
import yaml

EXIT_SUCCESS = 0
EXIT_VALIDATION_FAILED = 1
EXIT_USAGE_ERROR = 2
EXIT_RENDER_FAILED = 3
EXIT_REFERENCE_MISMATCH = 4


@dataclass(frozen=True, slots=True)
class ReferenceComparison:
    """Result of comparing a render against stored normalized evidence."""

    status: str
    reference_path: str
    mismatches: tuple[dict[str, object], ...] = ()

    def to_dict(self) -> dict[str, object]:
        payload: dict[str, object] = {
            'status': self.status,
            'reference_path': self.reference_path,
        }
        if self.mismatches:
            payload['mismatches'] = [dict(entry) for entry in self.mismatches]
        return payload


def build_parser() -> argparse.ArgumentParser:
    """Build the Cardiff CLI parser."""

    parser = argparse.ArgumentParser(
        prog='cardiff',
        description='Validate and render Cardiff business-card requests.',
    )
    subparsers = parser.add_subparsers(dest='command', required=True)

    validate_parser = subparsers.add_parser(
        'validate',
        help='Validate one JSON or YAML request against the canonical contract.',
    )
    _add_common_request_arguments(validate_parser)
    validate_parser.set_defaults(handler=run_validate)

    render_parser = subparsers.add_parser(
        'render',
        help='Render one PDF from a validated Cardiff request.',
    )
    _add_common_request_arguments(render_parser)
    render_parser.add_argument(
        '--output',
        required=True,
        metavar='PATH',
        help='Destination PDF path. Parent directories are created if needed.',
    )
    render_parser.add_argument(
        '--template-root',
        metavar='PATH',
        help='Optional approved template root override.',
    )
    render_parser.add_argument(
        '--reference-evidence',
        metavar='PATH',
        help='Optional normalized evidence JSON to compare against after a successful render.',
    )
    render_parser.add_argument(
        '--deterministic',
        action='store_true',
        help='Force the deterministic adapter even if xelatex is available.',
    )
    render_parser.set_defaults(handler=run_render)

    return parser


def main(argv: list[str] | None = None) -> int:
    """Run the Cardiff CLI and return a process exit code."""

    parser = build_parser()
    args = parser.parse_args(argv)
    return args.handler(args, stdout=sys.stdout, stderr=sys.stderr)


def run_validate(args: argparse.Namespace, *, stdout: TextIO, stderr: TextIO) -> int:
    """Execute the validate command."""

    request_path = _resolve_existing_file(args.request)
    if request_path is None:
        return _emit_cli_error(
            command='validate',
            diagnostics=(f"request file '{args.request}' does not exist",),
            source_path=_resolve_path(args.request),
            exit_code=EXIT_USAGE_ERROR,
            stdout=stdout,
            stderr=stderr,
        )

    asset_roots = _resolve_optional_paths(args.approved_asset_roots)
    raw_document = load_request_document(request_path)
    validation_result = parse_render_request(
        raw_document,
        approved_asset_roots=asset_roots or None,
    )

    source_payload = _source_payload(raw_document.source_name, raw_document.source_format)
    if validation_result.accepted:
        assert validation_result.request is not None
        payload = {
            'command': 'validate',
            'diagnostics': ['request accepted by the canonical contract'],
            'exit_code': EXIT_SUCCESS,
            'issue_count': 0,
            'issues': [],
            'source': source_payload,
            'status': 'accepted',
            'template_id': validation_result.request.template.template_id,
        }
        _emit_status(payload, stdout=stdout)
        _emit_lines(
            (
                f"Validated '{source_payload['path']}' for template '{validation_result.request.template.template_id}'.",
            ),
            stderr=stderr,
        )
        return EXIT_SUCCESS

    payload = {
        'command': 'validate',
        'diagnostics': [f"validation rejected with {len(validation_result.issues)} issue(s)"],
        'exit_code': EXIT_VALIDATION_FAILED,
        'issue_count': len(validation_result.issues),
        'issues': _issues_payload(validation_result.issues),
        'source': source_payload,
        'status': 'rejected',
        'template_id': _extract_template_id(raw_document.content),
    }
    _emit_status(payload, stdout=stdout)
    _emit_lines(
        [
            f"Validation rejected '{source_payload['path']}'.",
            *_format_issue_lines(validation_result.issues),
        ],
        stderr=stderr,
    )
    return EXIT_VALIDATION_FAILED


def run_render(args: argparse.Namespace, *, stdout: TextIO, stderr: TextIO) -> int:
    """Execute the render command."""

    request_path = _resolve_existing_file(args.request)
    output_path = _resolve_path(args.output)
    if request_path is None:
        return _emit_cli_error(
            command='render',
            diagnostics=(f"request file '{args.request}' does not exist",),
            source_path=_resolve_path(args.request),
            output_path=output_path,
            exit_code=EXIT_USAGE_ERROR,
            stdout=stdout,
            stderr=stderr,
        )

    template_root = _resolve_path(args.template_root) if args.template_root else None
    asset_roots = _resolve_optional_paths(args.approved_asset_roots)
    reference_path = None
    reference_payload = None
    if args.reference_evidence:
        reference_path = _resolve_existing_file(args.reference_evidence)
        if reference_path is None:
            return _emit_cli_error(
                command='render',
                diagnostics=(f"reference evidence file '{args.reference_evidence}' does not exist",),
                source_path=request_path,
                output_path=output_path,
                exit_code=EXIT_USAGE_ERROR,
                stdout=stdout,
                stderr=stderr,
            )
        reference_payload, reference_error = _load_reference_payload(reference_path)
        if reference_error is not None:
            return _emit_cli_error(
                command='render',
                diagnostics=(reference_error,),
                source_path=request_path,
                output_path=output_path,
                exit_code=EXIT_USAGE_ERROR,
                stdout=stdout,
                stderr=stderr,
            )

    raw_document = load_request_document(request_path)
    validation_result = parse_render_request(
        raw_document,
        approved_asset_roots=asset_roots or None,
    )
    source_payload = _source_payload(raw_document.source_name, raw_document.source_format)
    if not validation_result.accepted:
        payload = {
            'command': 'render',
            'diagnostics': ['render skipped because validation failed'],
            'exit_code': EXIT_VALIDATION_FAILED,
            'failure_stage': 'validation',
            'issue_count': len(validation_result.issues),
            'issues': _issues_payload(validation_result.issues),
            'output_path': output_path.as_posix(),
            'source': source_payload,
            'status': 'rejected',
            'template_id': _extract_template_id(raw_document.content),
        }
        _emit_status(payload, stdout=stdout)
        _emit_lines(
            [
                f"Render skipped because '{source_payload['path']}' failed validation.",
                *_format_issue_lines(validation_result.issues),
            ],
            stderr=stderr,
        )
        return EXIT_VALIDATION_FAILED

    assert validation_result.request is not None
    adapter = DeterministicTeXAdapter() if args.deterministic else get_default_tex_adapter()
    render_result = render_request_to_pdf(
        validation_result.request,
        output_path,
        template_root=template_root,
        approved_asset_roots=asset_roots or None,
        tex_adapter=adapter,
    )
    runtime_payload = {
        'name': adapter.runtime_name,
        'version': adapter.runtime_version,
    }
    if render_result.evidence is not None:
        runtime_payload = {
            'name': render_result.evidence.runtime_name,
            'version': render_result.evidence.runtime_version,
        }

    if not render_result.succeeded:
        payload = {
            'command': 'render',
            'diagnostics': list(render_result.diagnostics),
            'exit_code': EXIT_RENDER_FAILED,
            'failure_class': str(render_result.failure_class) if render_result.failure_class is not None else None,
            'failure_stage': 'render',
            'output_path': output_path.as_posix(),
            'runtime': runtime_payload,
            'source': source_payload,
            'status': 'failed',
            'template_id': render_result.template_id,
        }
        _emit_status(payload, stdout=stdout)
        _emit_lines(
            [
                f"Render failed for '{source_payload['path']}'.",
                *tuple(render_result.diagnostics),
            ],
            stderr=stderr,
        )
        return EXIT_RENDER_FAILED

    reference_comparison = None
    exit_code = EXIT_SUCCESS
    if reference_payload is not None:
        if render_result.evidence is None:
            reference_comparison = ReferenceComparison(
                status='mismatch',
                reference_path=reference_path.as_posix(),
                mismatches=(
                    {
                        'field': 'evidence',
                        'expected': 'normalized evidence payload',
                        'actual': None,
                    },
                ),
            )
            exit_code = EXIT_REFERENCE_MISMATCH
        else:
            reference_comparison = compare_reference_evidence(
                render_result.evidence.to_dict(),
                reference_payload,
                reference_path,
            )
            if reference_comparison.status != 'match':
                exit_code = EXIT_REFERENCE_MISMATCH

    payload = {
        'command': 'render',
        'diagnostics': list(render_result.diagnostics),
        'exit_code': exit_code,
        'output_path': render_result.output_path,
        'runtime': runtime_payload,
        'source': source_payload,
        'status': 'succeeded',
        'template_id': render_result.template_id,
    }
    if reference_comparison is not None:
        payload['reference_comparison'] = reference_comparison.to_dict()
    _emit_status(payload, stdout=stdout)

    notes = [
        f"Rendered '{render_result.template_id}' from '{source_payload['path']}' to '{render_result.output_path}'.",
    ]
    if reference_comparison is not None:
        if reference_comparison.status == 'match':
            notes.append(
                f"Reference evidence matched '{reference_comparison.reference_path}'."
            )
        else:
            notes.append(
                f"Reference evidence mismatch against '{reference_comparison.reference_path}'."
            )
            notes.extend(
                f"{entry['field']}: expected {entry['expected']!r}, actual {entry['actual']!r}"
                for entry in reference_comparison.mismatches
            )
    _emit_lines(notes, stderr=stderr)
    return exit_code


def compare_reference_evidence(
    current_payload: dict[str, object],
    reference_payload: dict[str, object],
    reference_path: Path,
) -> ReferenceComparison:
    """Compare current render evidence against a stored reference payload.

    Both payloads must have identical keys for a match. If either payload
    contains fields not present in the other, or if any field values differ,
    the comparison returns a mismatch.
    """

    mismatches: list[dict[str, object]] = []

    # Check for missing fields in current payload (present in reference but not current)
    for field_name in sorted(reference_payload):
        expected_value = reference_payload[field_name]
        if field_name not in current_payload:
            mismatches.append(
                {
                    'field': field_name,
                    'expected': expected_value,
                    'actual': None,
                }
            )
            continue

        actual_value = current_payload[field_name]
        if actual_value != expected_value:
            mismatches.append(
                {
                    'field': field_name,
                    'expected': expected_value,
                    'actual': actual_value,
                }
            )

    # Check for unexpected fields in current payload (present in current but not reference)
    unexpected_fields = set(current_payload.keys()) - set(reference_payload.keys())
    for field_name in sorted(unexpected_fields):
        mismatches.append(
            {
                'field': field_name,
                'expected': None,
                'actual': current_payload[field_name],
            }
        )

    return ReferenceComparison(
        status='match' if not mismatches else 'mismatch',
        reference_path=reference_path.as_posix(),
        mismatches=tuple(mismatches),
    )


def _add_common_request_arguments(parser: argparse.ArgumentParser) -> None:
    parser.add_argument('request', metavar='REQUEST', help='Path to the JSON or YAML request file.')
    parser.add_argument(
        '--approved-asset-root',
        action='append',
        default=[],
        dest='approved_asset_roots',
        metavar='PATH',
        help='Approved root for relative asset references. Repeat for multiple roots.',
    )


def _resolve_existing_file(raw_path: str) -> Path | None:
    path = _resolve_path(raw_path)
    if not path.exists() or not path.is_file():
        return None
    return path


def _resolve_optional_paths(raw_paths: list[str]) -> tuple[Path, ...]:
    return tuple(_resolve_path(raw_path) for raw_path in raw_paths)


def _resolve_path(raw_path: str | None) -> Path:
    if raw_path is None:
        raise ValueError('raw_path must not be None')
    return Path(raw_path).expanduser().resolve()


def _issues_payload(issues: tuple[object, ...]) -> list[dict[str, object]]:
    payload = []
    for issue in issues:
        payload.append(
            {
                'code': str(issue.code),
                'field': issue.field,
                'message': issue.message,
                'severity': issue.severity,
            }
        )
    return payload


def _format_issue_lines(issues: tuple[object, ...]) -> list[str]:
    return [f"{issue.field}: {issue.code} - {issue.message}" for issue in issues]


def _source_payload(path: str, source_format: str) -> dict[str, str]:
    return {
        'format': source_format,
        'path': Path(path).as_posix(),
    }


def _emit_cli_error(
    *,
    command: str,
    diagnostics: tuple[str, ...],
    source_path: Path,
    exit_code: int,
    stdout: TextIO,
    stderr: TextIO,
    output_path: Path | None = None,
) -> int:
    payload: dict[str, object] = {
        'command': command,
        'diagnostics': list(diagnostics),
        'exit_code': exit_code,
        'failure_stage': 'cli',
        'source': {'format': 'unknown', 'path': source_path.as_posix()},
        'status': 'failed',
    }
    if output_path is not None:
        payload['output_path'] = output_path.as_posix()
    _emit_status(payload, stdout=stdout)
    _emit_lines(diagnostics, stderr=stderr)
    return exit_code


def _load_reference_payload(reference_path: Path) -> tuple[dict[str, object] | None, str | None]:
    try:
        payload = json.loads(reference_path.read_text(encoding='utf-8'))
    except (OSError, json.JSONDecodeError) as error:
        return None, f"failed to load reference evidence '{reference_path.as_posix()}': {error}"
    if not isinstance(payload, dict):
        return None, f"reference evidence '{reference_path.as_posix()}' must decode into a JSON object"
    return payload, None


def _extract_template_id(raw_content: str) -> str | None:
    try:
        payload = json.loads(raw_content) if raw_content.lstrip().startswith('{') else yaml.safe_load(raw_content)
    except (json.JSONDecodeError, yaml.YAMLError):
        return None
    if isinstance(payload, dict):
        template_payload = payload.get('template')
        if isinstance(template_payload, dict):
            template_id = template_payload.get('id')
            if isinstance(template_id, str):
                return template_id
    return None


def _emit_status(payload: dict[str, object], *, stdout: TextIO) -> None:
    stdout.write(json.dumps(payload, sort_keys=True) + '\n')


def _emit_lines(lines: list[str] | tuple[str, ...], *, stderr: TextIO) -> None:
    for line in lines:
        stderr.write(f'{line}\n')


if __name__ == '__main__':
    raise SystemExit(main())
