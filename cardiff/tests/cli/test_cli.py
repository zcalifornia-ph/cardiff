from __future__ import annotations

import io
import subprocess
import json
import sys
from pathlib import Path
from contextlib import redirect_stderr, redirect_stdout

from cardiff.cli import main, compare_reference_evidence

PROJECT_ROOT = Path(__file__).resolve().parents[2]
FIXTURES_ROOT = Path(__file__).resolve().parents[1] / 'fixtures'
APPROVED_ASSETS = FIXTURES_ROOT / 'approved-assets'
APPROVED_SAMPLES = FIXTURES_ROOT / 'approved-samples' / 'business-card'
REFERENCE_EVIDENCE = APPROVED_SAMPLES / 'reference-evidence.json'
VALID_REQUEST = FIXTURES_ROOT / 'requests' / 'valid-request.yaml'
INVALID_REQUEST = FIXTURES_ROOT / 'requests' / 'invalid-request-missing-email.yaml'
OUTPUT_PATH = APPROVED_SAMPLES / 'determinism-output.pdf'
INVALID_OUTPUT_PATH = APPROVED_SAMPLES / 'invalid-render-output.pdf'


def _run_cli(argv: list[str]) -> tuple[int, dict[str, object], str]:
    stdout = io.StringIO()
    stderr = io.StringIO()
    with redirect_stdout(stdout), redirect_stderr(stderr):
        exit_code = main(argv)
    payload = json.loads(stdout.getvalue())
    return exit_code, payload, stderr.getvalue()


def test_validate_command_accepts_the_approved_sample():
    exit_code, payload, stderr = _run_cli(
        [
            'validate',
            VALID_REQUEST.as_posix(),
            '--approved-asset-root',
            APPROVED_ASSETS.as_posix(),
        ]
    )

    assert exit_code == 0
    assert payload['command'] == 'validate'
    assert payload['status'] == 'accepted'
    assert payload['template_id'] == 'business-card'
    assert payload['source']['format'] == 'yaml'
    assert payload['exit_code'] == 0
    assert payload['issues'] == []
    assert 'Validated' in stderr


def test_validate_command_returns_structured_issues_for_invalid_request():
    exit_code, payload, stderr = _run_cli(
        [
            'validate',
            INVALID_REQUEST.as_posix(),
            '--approved-asset-root',
            APPROVED_ASSETS.as_posix(),
        ]
    )

    assert exit_code == 1
    assert payload['command'] == 'validate'
    assert payload['status'] == 'rejected'
    assert payload['template_id'] == 'business-card'
    assert payload['exit_code'] == 1
    assert payload['issue_count'] >= 1
    assert any(issue['field'] == 'identity.email' for issue in payload['issues'])
    assert 'Validation rejected' in stderr


def test_render_command_writes_the_requested_pdf_and_reports_runtime_metadata():
    exit_code, payload, stderr = _run_cli(
        [
            'render',
            VALID_REQUEST.as_posix(),
            '--approved-asset-root',
            APPROVED_ASSETS.as_posix(),
            '--output',
            OUTPUT_PATH.as_posix(),
            '--deterministic',
        ]
    )

    assert exit_code == 0
    assert payload['command'] == 'render'
    assert payload['status'] == 'succeeded'
    assert payload['template_id'] == 'business-card'
    assert payload['runtime']['name'] == 'deterministic-tex-adapter'
    assert payload['runtime']['version'] == '1'
    assert payload['output_path'] == OUTPUT_PATH.as_posix()
    assert OUTPUT_PATH.read_bytes().startswith(b'%PDF-1.4')
    assert 'Rendered' in stderr


def test_render_command_can_compare_against_reference_evidence():
    exit_code, payload, _ = _run_cli(
        [
            'render',
            VALID_REQUEST.as_posix(),
            '--approved-asset-root',
            APPROVED_ASSETS.as_posix(),
            '--output',
            OUTPUT_PATH.as_posix(),
            '--deterministic',
            '--reference-evidence',
            REFERENCE_EVIDENCE.as_posix(),
        ]
    )

    assert exit_code == 0
    assert payload['status'] == 'succeeded'
    assert payload['reference_comparison']['status'] == 'match'
    assert payload['exit_code'] == 0


def test_render_command_stops_when_validation_fails():
    assert not INVALID_OUTPUT_PATH.exists()

    exit_code, payload, stderr = _run_cli(
        [
            'render',
            INVALID_REQUEST.as_posix(),
            '--approved-asset-root',
            APPROVED_ASSETS.as_posix(),
            '--output',
            INVALID_OUTPUT_PATH.as_posix(),
            '--deterministic',
        ]
    )

    assert exit_code == 1
    assert payload['command'] == 'render'
    assert payload['status'] == 'rejected'
    assert payload['failure_stage'] == 'validation'
    assert not INVALID_OUTPUT_PATH.exists()
    assert 'failed validation' in stderr


def test_python_dash_m_entrypoint_supports_validate_command():
    completed = subprocess.run(
        [
            sys.executable,
            '-m',
            'cardiff',
            'validate',
            VALID_REQUEST.as_posix(),
            '--approved-asset-root',
            APPROVED_ASSETS.as_posix(),
        ],
        cwd=PROJECT_ROOT,
        capture_output=True,
        text=True,
        check=False,
    )

    assert completed.returncode == 0
    payload = json.loads(completed.stdout)
    assert payload['command'] == 'validate'
    assert payload['status'] == 'accepted'
    assert payload['template_id'] == 'business-card'


